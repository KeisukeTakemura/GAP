    public Point getTileCollision(Player player,double newX, double newY,int[][] map){
        newX = Math.ceil(newX);
        newY = Math.ceil(newY);

        double fromX = Math.min(player.getX(), newX);
        double fromY = Math.min(player.getY(), newY);
        double toX = Math.max(player.getX(), newX);
        double toY = Math.max(player.getY(), newY);

        int fromTileX = pixelsToTiles(fromX);
        int fromTileY = pixelsToTiles(fromY);
        int toTileX = pixelsToTiles(toX + player.getSize() - 1);
        int toTileY = pixelsToTiles(toY + player.getSize() - 1);

        for (int x = fromTileX; x <= toTileX; x++) {
            for (int y = fromTileY; y <= toTileY; y++) {

                if (x < 0 || x >= COL) {
                    return new Point(x, y);
                }
                if (y < 0 || y >= ROW) {
                    return new Point(x, y);
                }
                if (map[y][x] == 1) {
                    return new Point(x, y);
                }
            }
        }

        return null;
    }




